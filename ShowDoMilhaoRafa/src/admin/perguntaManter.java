    private void botaoexcluir3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoexcluir3ActionPerformed
        if (txtEnunciado.getText().isEmpty() == false) {
            if (lista.size() >= 0) {
                PerguntaDao dao = new PerguntaDao();
                Boolean deucerto = dao.remover(lista.get(posicao));
                if (deucerto == true) {
                    Limpar();
                    posicao = 0;
                    lista = dao.listar();
                    JOptionPane.showMessageDialog(null, "Excluído!");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao excluir!");
            }
        }
    }//GEN-LAST:event_botaoexcluir3ActionPerformed

    private void botaoconsultar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoconsultar3ActionPerformed
        Integer id = Integer.parseInt(JOptionPane.showInputDialog("Digite o ID da pergunta a ser pesquisada: "));
        boolean encontrou = false;
        Integer posicaoachou = 0;
        for (Pergunta pergunta : lista) {
            if (id == (pergunta.getId())) {
                encontrou = true;
                posicao = posicaoachou;
                txtEnunciado.setText(pergunta.getPergunta());
                txtA.setText(pergunta.getA());
                txtB.setText(pergunta.getB());
                txtC.setText(pergunta.getC());
                txtD.setText(pergunta.getD());
                cbxNivel.setSelectedItem(pergunta.getNivel());
                cbxResposta.setSelectedItem(pergunta.getCerta());
                txtID.setText(pergunta.getId().toString());
                posicao = posicaoachou;
                break;
            }
            posicaoachou++;
        }
        if (encontrou == true) {
            JOptionPane.showMessageDialog(null, "Encontramos sua pergunta.");
        } else {
            JOptionPane.showMessageDialog(null, "Não encontramos sua pergunta ou ela não está cadastrada.");
            Limpar();
        }
    }//GEN-LAST:event_botaoconsultar3ActionPerformed

