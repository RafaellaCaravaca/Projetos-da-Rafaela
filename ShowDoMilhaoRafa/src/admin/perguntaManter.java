    private void botaoproximo3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoproximo3ActionPerformed
        botaoprimeiro3.setEnabled(true);
        botaoanterior3.setEnabled(true);
        botaoproximo3.setEnabled(true);
        btnUltimo3.setEnabled(true);
        if (posicao < lista.size() - 1) {
            posicao = posicao + 1;
            Pergunta elemento = lista.get(posicao);
            txtEnunciado.setText(elemento.getPergunta());
            txtA.setText(elemento.getA());
            txtB.setText(elemento.getB());
            txtC.setText(elemento.getC());
            txtD.setText(elemento.getD());
            cbxNivel.setSelectedItem(elemento.getNivel().toString());
            cbxResposta.setSelectedItem(elemento.getCerta());
            txtID.setText(elemento.getId().toString());
        }
        if (lista.size() - 1 == posicao) {
            botaoproximo3.setEnabled(false);
            btnUltimo3.setEnabled(false);
        }
    }//GEN-LAST:event_botaoproximo3ActionPerformed

    private void btnUltimo3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUltimo3ActionPerformed
        botaoprimeiro3.setEnabled(true);
        botaoanterior3.setEnabled(true);
        botaoproximo3.setEnabled(false);
        btnUltimo3.setEnabled(false);
        if (lista.size() > 0) {
            posicao = lista.size() - 1;
            Pergunta elemento = lista.get(posicao);
            txtEnunciado.setText(elemento.getPergunta());
            txtA.setText(elemento.getA());
            txtB.setText(elemento.getB());
            txtC.setText(elemento.getC());
            txtD.setText(elemento.getD());
            cbxNivel.setSelectedItem(elemento.getNivel().toString());
            cbxResposta.setSelectedItem(elemento.getCerta());
            txtID.setText(elemento.getId().toString());
        }
        if (lista.size() - 1 == posicao && lista.size() == 1) {
            botaoproximo3.setEnabled(false);
            btnUltimo3.setEnabled(false);
            botaoanterior3.setEnabled(false);
        }
    }//GEN-LAST:event_btnUltimo3ActionPerformed

